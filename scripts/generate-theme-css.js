/**
 * Generates global.css from the active @sudobility/design theme.
 *
 * Emits the Tailwind directives plus the theme's CSS custom properties as a
 * `:root` (light) block. NativeWind reads these variables so semantic classes
 * (bg-primary, text-foreground, border-border, ...) resolve to the theme's
 * colors. Runtime light/dark switching is done by ThemeVarsProvider via vars()
 * (NativeWind can't switch CSS-variable blocks on native), so only the light
 * :root block is needed here.
 *
 * Runs from `prestart`. The theme here MUST match:
 *   - tailwind.config.js  -> createTailwindPreset() + defaultTheme radius
 *   - src/config/designTheme.ts -> configureTheme(defaultTheme, { native: true })
 *   - src/config/themeVars.ts   -> vars(defaultTheme.light / .dark)
 * To switch the design style (default, neo-brutalism, ...) change all of them.
 */
const fs = require('fs');
const path = require('path');
const { generateThemeCSS } = require('@sudobility/design');
const { defaultTheme } = require('@sudobility/design/themes');

const activeTheme = defaultTheme;

// generateThemeCSS emits ":root {light} .dark {dark}"; keep only :root by
// slicing off everything from the ".dark" selector onward (robust to format).
const full = generateThemeCSS(activeTheme);
const darkIdx = full.indexOf('.dark');
const lightRoot =
  (darkIdx >= 0 ? full.slice(0, darkIdx).trimEnd() : full.trimEnd()) + '\n';

const css = `@tailwind base;
@tailwind components;
@tailwind utilities;

/* Generated by scripts/generate-theme-css.js from @sudobility/design theme: ${activeTheme.name}. Do not edit by hand. */
${lightRoot}`;

const out = path.join(__dirname, '..', 'global.css');
fs.writeFileSync(out, css);
console.log(`Generated global.css for design theme: ${activeTheme.name}`);
